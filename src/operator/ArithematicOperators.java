package operator;

public class ArithematicOperators {

	public static void main(String[] args) {
		// Additive
		int cow = 4;
		int human = 2;
		int totalLegs = cow + human;
		System.out.println(totalLegs);
		
		// Subtraction
		int apples = 10;
		int eaten = 3;
		int left = apples - eaten;
		System.out.println(left);

		// Multiplication
		int box = 5;
		int items = 3;
		int total = box * items;
		System.out.println(total);

		// Division
		int cake = 8;
		int people = 2;
		int each = cake / people;
		System.out.println(each);

		// Remainder
		int candy = 7;
		int kids = 3;
		int remain = candy % kids;
		System.out.println(remain);

	}

}
