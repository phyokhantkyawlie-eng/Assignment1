package bankingsystem;

public class Address {
	
	
	
	private String _city;
	
	private String _country;

	public Address(String _city, String _country) {
		this._city = _city;
		this._country = _country;
	}

	public String get_city() {
		return _city;
	}

	public String get_country() {
		return _country;
	}
	
	
	
	
	
	

}