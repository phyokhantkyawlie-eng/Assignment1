package controlflow;


// do while loop


/**
 * 
 * 	  starting point 
 * 
 *    do {
 *    
 *    output statement
 *    
 *    rate;
 *    
 *    }while (ending point);
 *    
 *    
 * 
 * 
 */

public class InterationThree {
	
	public static void main(String[] args) {
		
		// starting point
		int i = 105;
		
		
		do {
			
			System.out.println("I value is "+ i); // output statement
			
			i++;// rate
			
			
		}while (i <= 100); // ending point
		
		/**
		 * 
		 *    starting point 			output 				rate 			while (ending point)
		 *    
		 *    105						I value is 105  	106 			106  <= 100 -> false 
		 *    stop the loop 
		 *    
		 *    
		 * 
		 */
		
		
		
		
		
		
		
		
	}

}