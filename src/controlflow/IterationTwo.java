package controlflow;

public class IterationTwo {
// While Looping
	
	/*
	 *     starting point
	 *      
	 *     while (ending point/ condition ) {
	 *     		
	 *     		output or statement
	 *     
	 *     		rate
	 *     }
	 * 
	 */
	public static void main(String[] args) {
		
		
		
		// starting point
		int i = 0 ; 
			 // ending
		while (i < 5 ) {
			
			
			//output
			System.out.println("Hello This is Java");
			
			// rate
			i++;   
		}
		
		// Debug while loop 
		
		/**						
		 * 						i < 5 
		 *  start point 		end point		output 		rate ( i ++ ) 
		 *   int i = 0 			0 < 5 = true 	Yes			1 
		 *		1 				1 < 5 = true 	Yes 		2   
		 *   	2 				2 < 5 = true 	Yes 		3
		 *   	3 				3 < 5 = true 	Yes		    4		
		 *   	4 				4 < 5 = true 	Yes 		5 
		 *   	5 				5 < 5 = false   -> stop
		 */

	}

}