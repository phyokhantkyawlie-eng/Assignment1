package controlflow;
// For looping 
public class IterationOne {
	
	/**
	 * 
	 * 		for (starting point ; ending point (expression) ; rate ) {
	 * 
								output statement or doing process 
			}
	 */


	public static void main(String[] args) {
		
	// 		starting      ending  rate 
		for (int i = 0 ; i < 10  ; i++  ) {
			
			System.out.println("Hello Java Developer");
			
		}
		
		
		
		/*		
		 *  int i = 0 		
		 * 						condition 				i++ (postfix) 
		 *    starting   		ending 	  		   increasement     	output 
		 * 		0 			   0 < 10  = true 	 	  1 				yes	  -> 1 
		 * 		1 			   1 < 10 = true 		  2 				yes -> 2
		 * 		2  				2 < 10 = true 		  3 				yes -> 3 
		 * 		3               3 < 10 = true 		  4 				yes -> 4 
		 * 		4 				4 < 10 = true 		  5 				yes -> 5
		 * 		5 				5 < 10 = true 		  6 				yes -> 6
		 * 		6 				6 < 10 = true 		  7 			    yes -> 7 
		 * 		7 				7< 10  = true 		  8					yes -> 8 
		 * 		8 				8 < 10 =  true 		  9 					yes -> 9 
		 *      9 				9 < 10 = true 		  10 						yes -> 10 
		 *      10              10 < 10 = false 		looping stop 
		 * 
		 * 
		 * 
		 */
		
		
	

	}

}